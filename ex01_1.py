'''
Operadores relacionais 
== igualdade
>  maior 
>= maior ou igual
<  menor 
<= menor ou igual
!= diferente
'''

print( 2 ==2 ) # verdadeiro , True
print( 3 > 2)  # verdadeiro , True
print( 2 >= 2) # verdadeiro , True
print( 2 < 3)  # verdadeiro , True
print( 3 <= 3) # verdadeiro , True
print( 4 != 3) # verdadeiro , True
print( 4 > 3 and 4 >= 3) # verdadeiro , True
