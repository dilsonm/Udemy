'''
Operadores aritmétios
+, , -, *, /, //, **, %, ()
'''
print('Multiplicação * ', 10 * 10)
print('Adição + ', 10 + 10)
print('Subtração - ', 10 - 5)
print('Divisão / ', 10 / 2)
print('Divisão inteira // ', 10 // 3)
print('Exponenciação ** ', 10 ** 3)
print('Resto da divisão % ', 10 % 3)
print('Prioridade dos parâmetros: ', (2+2) * 10 % 3)
